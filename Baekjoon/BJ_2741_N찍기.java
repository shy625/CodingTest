import java.util.Scanner;

public class BJ_2741_N찍기 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        int n = sc.nextInt();

        for(int i = 1; i <= n; i++) {
            System.out.println(i);
        }
    }
}